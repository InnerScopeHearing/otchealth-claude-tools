#!/usr/bin/env node
// render-portfolio.mjs — render the portfolio command-center to a Miro board.
// Reads app.manifest.json files if present; otherwise uses the seed below with
// known facts and honest "not yet wired" status (reflecting the current gap).
// Usage:  MIRO_TOKEN=<token> node render-portfolio.mjs
// Non-PHI content only.

import { createBoard, text, shape } from "./_miro.mjs";

// Seed status from CLAUDE.md / README / the gap analysis. Update as real
// app.manifest.json files land (then this reads them directly).
const APPS = [
  { name: "iHEARtest", ring: "non-PHI", type: "hybrid", kits: "build, prelaunch, launch", gates: "tests ✗ · posthog ✗ · OTA capgo (planned)" },
  { name: "AWARE", ring: "non-PHI", type: "hybrid", kits: "build", gates: "tests ✗ · posthog ✗ · OTA ✗" },
  { name: "Companion", ring: "non-PHI", type: "hybrid", kits: "build", gates: "tests ✗ · posthog ✗ · on-device LLM (planned)" },
  { name: "InnerEase", ring: "non-PHI", type: "hybrid", kits: "early (CLAUDE.md only)", gates: "tests ✗ · posthog ✗" },
  { name: "MedReview", ring: "PHI", type: "B2B / service", kits: "playbook", gates: "BAA needed · self-host n8n trigger · tests ✗" },
  { name: "OTCHealthMart", ring: "non-PHI", type: "web-commerce", kits: "marketing", gates: "Shopify live · tests-lite ✗" },
];

const XS = [-720, 0, 720];
const YS = [-230, 70];

async function main() {
  const b = await createBoard(
    "OTCHealth — Portfolio Command Center",
    "Auto-generated by the diagrammer. Seeded status pending real app.manifest.json. Non-PHI."
  );
  const board = b.id;
  console.log("Board created:", b.viewLink);

  await text(board, "<p><strong>OTCHealth Portfolio Command Center</strong></p>", { x: 0, y: -440, font: "34" });
  await text(board, "<p>ring · type · kits adopted · gate status (seeded, pending live app.manifest.json)</p>", { x: 0, y: -380, font: "15" });

  for (let i = 0; i < APPS.length; i++) {
    const a = APPS[i];
    const x = XS[i % 3];
    const y = YS[Math.floor(i / 3)];
    const fill = a.ring === "PHI" ? "#ffe6e6" : "#e6f0ff";
    const content =
      `<p><strong>${a.name}</strong></p>` +
      `<p>ring: ${a.ring} · ${a.type}</p>` +
      `<p>kits: ${a.kits}</p>` +
      `<p>${a.gates}</p>`;
    await shape(board, content, { x, y, w: 460, h: 210, fill });
  }

  await shape(
    board,
    "<p><strong>Legend:</strong> blue = non-PHI ring · red = PHI ring (BAA required). <strong>Gap:</strong> 0 automated tests and 0 PostHog/flags across the consumer apps today. PostHog ($50k credits) + the web-first test stack close this; Guardian enforces the PHI ring.</p>",
    { x: 0, y: 320, w: 2000, h: 120, fill: "#f5f5f7" }
  );

  console.log("Done. Open:", b.viewLink);
}

main().catch((e) => { console.error(e.message); process.exit(1); });
